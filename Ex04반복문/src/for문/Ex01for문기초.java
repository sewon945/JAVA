package for문;

import java.util.Scanner;

public class Ex01for문기초 {

	public static void main(String[] args) {

		// for문은 언제까지 반복할 지 모르는 반복문이 아니라 끝을 아는 반복문임
		// for문에서는 i나 j로 변수를 많이 준다!
		
		
		// 1부터 10까지 출력
		for (int i = 1; i < 11; i++) { // 1부터 10이기에 편하게 1부터 시작인 1로 초기화구문 작성
			System.out.print(i + " ");
			
		}

		

		
	}

}
